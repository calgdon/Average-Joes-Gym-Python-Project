class Member:

    def __init__(self, first_name, last_name, date_of_birth, address, tel_number, email, platinum_member, id=None):
        self.first_name = first_name
        self.last_name = last_name
        self.date_of_birth = date_of_birth
        self.address = address
        self.tel_number = tel_number
        self.email = email
        self.platinum_member = platinum_member
        self.id = id
